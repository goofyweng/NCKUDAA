#include <iostream>
using namespace std;

int main()
{
  int x, y;
  x = 3;
  y == 5; //evaluates to false
  //the (=) assigns the value on the right-hand to the variable on its left,
  //while (==) compares whether the values on both sides of the operator are equal.
  if (y == 5)
  {
    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
  }
  return 0;
}
